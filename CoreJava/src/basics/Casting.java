package basics;

public class Casting {

	public static void main(String[] args) {

		int a=10;
		
		double d=a; // upcasting
		
		double c=10.20;
		
		int f=(int)c; // downcasting
		
		int po=555555555;
		
		long oppp= 5555555555L;
		
		po=(int)oppp;
		
		//System.out.println(po);
		
		System.out.println("f value is "+f);
		System.out.println("c value is "+c);
		
		
		// number family, character family, boolean 
		
		
		
	}

}
